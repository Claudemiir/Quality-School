    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conect = (Connection)DriverManager.getConnection("jdbc:mysql://localhost:3307/sistema","root","");
            
            String sql = "insert into cadastroprofessor (nomecompleto, datanascimento, disciplina,"
                    + "cpf, email, telefone, id, salario, estadocivil) "
                    + "values(?,?,?,?,?,?,?,?,?)";
            
            PreparedStatement ptstmt = (PreparedStatement)conect.prepareStatement(sql);
            ptstmt.setString(1,txtNomeCompleto.getText());
            ptstmt.setString(2,txtData.getText());
            ptstmt.setString(3,txtDisciplina.getText());
            ptstmt.setString(4,txtCpf.getText());
            ptstmt.setString(5,txtEmail.getText());
            ptstmt.setString(6,txtTelefone.getText());
            ptstmt.setString(7,txtId.getText());
            ptstmt.setString(8,txtSalario.getText());
            ptstmt.setString(9,txtEstado.getText());
              ptstmt.executeUpdate();
           
              JOptionPane.showMessageDialog(null, "CADASTRAMENTO FEITO COM SUCESSO");
            conect.close();
            
        } catch (HeadlessException | ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, "FALHA NO CADASTRAMENTO!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

